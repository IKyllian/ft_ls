#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	int				i;

	i = 0;
	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while ((p1[i] == p2[i]) && --n)
		i++;
	if (p1[i] != p2[i])
		return (p1[i] - p2[i]);
	return (0);
}
